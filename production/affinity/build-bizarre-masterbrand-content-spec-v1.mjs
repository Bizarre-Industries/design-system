function exactSourceIds(profileData, subject) {
  const defaults = categoryDefaults[subject.categoryId];
  if (!defaults) throw new Error(`No category defaults for ${subject.id}`);
  return [...new Set([...defaults.sources, ...profileData.sourceIds])];
}

function sourceMapping(sourceId, sourceRegistryById, subject) {
  const source = sourceRegistryById.get(sourceId);
  if (!source) throw new Error(`Unknown source ${sourceId} on ${subject.id}`);
  return {
    sourceId,
    path: source.path,
    sha256: source.sha256,
    classification: source.classification,
    role: source.role,
    subjectUse: `${subject.exactLabel}: source used to produce or govern this subject's documented content and visual recipe.`,
  };
}

function distanceChecks(profileData, subject) {
  return {
    far: `${subject.exactLabel} / FAR — ${profileData.focalSpecimen} The dominant silhouette, exact mark context when present, and singular Signal cue must read without close text.`,
    normal: `${subject.exactLabel} / NORMAL — ${profileData.composition} Functional hierarchy, state, labels, and representation become readable.`,
    close: `${subject.exactLabel} / CLOSE — Inspect native construction, source labels, hashes, material/process details, bilingual behavior, and provisional or evidence status.`,
  };
}

function parentAndSiblingText(subject, allSubjects, profileData) {
  const siblings = allSubjects.filter((candidate) => candidate.categoryId === subject.categoryId && candidate.id !== subject.id);
  const siblingLabels = siblings.map((candidate) => candidate.exactLabel).join('; ');
  const dependencies = profileData.dependencyIds.length ? profileData.dependencyIds.join(', ') : 'none beyond the listed canonical sources';
  return `${subject.exactLabel} belongs to ${subject.categoryId} / ${subject.categoryName}. Explicit dependencies: ${dependencies}. Siblings: ${siblingLabels || 'none; this is the only permanent subject in its category'}. Canonical sources are the exact source mappings stored on this subject; no overview, Figma page, Affinity artboard, or Figma Make frame supersedes them.`;
}

function productionText(subject, profileData, defaults, governance, usesProvisionalInputs, usesRejectedInputs) {
  const provisionalClause = usesRejectedInputs
    ? 'This recipe maps a rejected noncanonical source for lineage or comparison: it may appear only locked and visibly rejected, and no export containing or reproducing that geometry is publishable.'
    : usesProvisionalInputs
      ? 'This recipe uses governed-provisional inputs: the documentation page may follow its manifest publication status, but any export that embeds a provisional asset remains nonpublishable until that input is promoted with evidence.'
      : 'This recipe has no governed-provisional file input in its exact source map; its export still follows the subject verification and publication gates.';
  return `${subject.exactLabel}: ${profileData.production ?? defaults.production} ${provisionalClause} Every Gravity Well instance keeps the exact original paths, is entirely Signal Lime on Void or entirely black on Signal/Paper, and receives no gradient, spectrum, material, glow, foil, bevel, or mixed internal color. Every Continuous Lens Aperture silhouette follows the smooth tangent-continuous asymmetric-oval direction; machined bevels remain outside the outline.`;
}

